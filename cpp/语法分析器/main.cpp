#include "loader.h"
#include "parse.h"
int main(int argc, char* argv[]) {
	if (argc < 6) {  // 没有文件路径参数传入，只进行编译
		cout << "Usage: " << argv[0] << " <filename>\n";
		exit(0);
	}
	loadGrams(argv[1]), loadTable(argv[2]), loadMap(argv[3]), loadLex(argv[4]), loadTree(argv[5]);
	cout << "---Parse---\n";
	parse();
	cout << "---Tree---\n";
	printTree();
	while (!Ns.empty()) {
		delete Ns.top();
		Ns.pop();
	}
	return 0;
}